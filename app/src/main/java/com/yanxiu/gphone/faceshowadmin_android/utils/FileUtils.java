    public static String MD5Helper(String str) {
        try {
            MessageDigest messageDigest = MessageDigest.getInstance("MD5");
            messageDigest.reset();
            messageDigest.update(str.getBytes("UTF-8"));
            byte[] byteArray = messageDigest.digest();
            StringBuilder sb = new StringBuilder();
            for (int i = 0; i < byteArray.length; i++) {
                if (Integer.toHexString(0xFF & byteArray[i]).length() == 1) {
                    sb.append("0").append(Integer.toHexString(0xFF & byteArray[i]));
                } else {
                    sb.append(Integer.toHexString(0xFF & byteArray[i]));
                }
            }
            return sb.toString();
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
        } catch (UnsupportedEncodingException e) {
            e.printStackTrace();
        }

        throw new RuntimeException("no device Id");
    }

    public static String getExternalStorageAbsolutePath(String urlStr) {
        int position = urlStr.lastIndexOf(Constants.CHARACTER_SLASH);
        if (position > 0) {
            urlStr = urlStr.substring(position + 1);
        }
        String dir = getLoadExternalDir();
        File file = new File(dir);
        if (!file.exists())
            file.mkdirs();
        return dir + urlStr;
    }

    private static String getLoadExternalDir() {
        String path = Environment.getExternalStorageDirectory()
                .getAbsolutePath();
        StringBuffer mBuffer = new StringBuffer();
        mBuffer.append(path);
        mBuffer.append(Constants.DIR_APK);
        return mBuffer.toString();
    }

